#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const root = process.cwd();
const scanRoots = ["app/core-logic", "app/adapters"];
const outputPath = path.join(root, "docs/architecture/redux-action-map.md");

const toPosix = (p) => p.split(path.sep).join("/");

const walk = (dir) => {
	if (!fs.existsSync(dir)) return [];
	const entries = fs.readdirSync(dir, { withFileTypes: true });
	return entries.flatMap((entry) => {
		const full = path.join(dir, entry.name);
		if (entry.isDirectory()) return walk(full);
		if (!/\.(ts|tsx)$/.test(entry.name)) return [];
		return [full];
	});
};

const files = scanRoots.flatMap((scanRoot) => walk(path.join(root, scanRoot)));

const actions = new Map();
const producers = new Map();
const listeners = new Map();
const reducers = new Map();
const matcherListeners = new Map();

const addRef = (map, symbol, file) => {
	if (!symbol) return;
	const rel = toPosix(path.relative(root, file));
	if (!map.has(symbol)) map.set(symbol, new Set());
	map.get(symbol).add(rel);
};

const read = (file) =>
	fs
		.readFileSync(file, "utf8")
		.replace(/^\s*\/\/.*$/gm, "");

for (const file of files) {
	const source = read(file);
	const rel = toPosix(path.relative(root, file));

	for (const match of source.matchAll(/export\s+const\s+(\w+)\s*=\s*createAction(?:<[\s\S]*?>)?\(\s*["'`]([^"'`]+)["'`]/g)) {
		const [, symbol, type] = match;
		actions.set(symbol, { symbol, type, file: rel });
	}

	for (const match of source.matchAll(/(?:^|[^\w.])(?:api\.dispatch|store\.dispatch|dispatch)\s*\(\s*(\w+)\s*\(/g)) {
		addRef(producers, match[1], file);
	}

	for (const match of source.matchAll(/actionCreator\s*:\s*(\w+)/g)) {
		addRef(listeners, match[1], file);
	}

	for (const match of source.matchAll(/\.addCase\(\s*(\w+)/g)) {
		addRef(reducers, match[1], file);
	}

	for (const match of source.matchAll(/matcher\s*:\s*isAnyOf\(([\s\S]*?)\)/g)) {
		const args = match[1]
			.split(",")
			.map((x) => x.trim())
			.filter(Boolean);
		for (const arg of args) {
			const symbol = arg.match(/^(\w+)/)?.[1];
			addRef(matcherListeners, symbol, file);
		}
	}
}

const refs = (map, symbol) =>
	Array.from(map.get(symbol) ?? [])
		.sort()
		.map((file) => `\`${file}\``)
		.join("<br>") || "-";

const listenerRefs = (symbol) => {
	const direct = Array.from(listeners.get(symbol) ?? []);
	const matcher = Array.from(matcherListeners.get(symbol) ?? []);
	const all = Array.from(new Set([...direct, ...matcher])).sort();
	return all.map((file) => `\`${file}\``).join("<br>") || "-";
};

const rows = Array.from(actions.values()).sort((a, b) => a.type.localeCompare(b.type));

const orphanDispatches = Array.from(producers.keys())
	.filter((symbol) => !actions.has(symbol))
	.sort();

const lines = [
	"# Redux Action Map",
	"",
	"> Generated by `npm run redux:map`. Do not edit this file by hand.",
	"",
	"## How To Read",
	"",
	"- **Action** is the exported `createAction` symbol.",
	"- **Type** is the Redux action type string.",
	"- **Producers** are files that dispatch the action.",
	"- **Listeners** are files that register the action in listener middleware, including `isAnyOf` matchers.",
	"- **Reducers** are files that handle the action with `builder.addCase`.",
	"",
	"This is a static map. It intentionally does not infer runtime ordering, thunk internals, or dynamic action creators.",
	"",
	"## Action Storming Table",
	"",
	"| Action | Type | Defined In | Producers | Listeners | Reducers |",
	"|---|---|---|---|---|---|",
];

for (const action of rows) {
	lines.push(
		`| \`${action.symbol}\` | \`${action.type}\` | \`${action.file}\` | ${refs(producers, action.symbol)} | ${listenerRefs(action.symbol)} | ${refs(reducers, action.symbol)} |`,
	);
}

lines.push("", "## Dispatches Without Local `createAction` Definition", "");

if (!orphanDispatches.length) {
	lines.push("None detected.");
} else {
	lines.push("| Symbol | Producers |", "|---|---|");
	for (const symbol of orphanDispatches) {
		lines.push(`| \`${symbol}\` | ${refs(producers, symbol)} |`);
	}
}

lines.push("", "## Suggested Event Storming Use", "");
lines.push(
	"1. Start from an action type that represents a user intention, for example `UI/COMMENT/CREATE`.",
	"2. Read the producer column to find the screen/view model entry point.",
	"3. Read the listener column to find orchestration.",
	"4. Read the reducer column to find read-model state transitions.",
	"5. Treat actions with many listeners or reducers as candidates for explicit process naming.",
);

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, `${lines.join("\n")}\n`);
console.log(`Wrote ${toPosix(path.relative(root, outputPath))}`);
